<?php
/*
练习：使用for循环打印出100以内的质数——难度非常大！
外循环-$i：2/3/4/5...99
内循环-$j：2~$i-1   	
质数判定： 如果内循环退出时$j==$i，说明$i是质数；反之$j<$i，说明$i不是质数
*/

#外层循环: 2/3/4/5...99		  
for($i=2; $i<100; $i++){
	#$i：即将要判定是否为质数的数 —— 被除数
	#思路：2/3/4...$i-1依次去除$i，只要有一个能整除，说明不是质数，退出循环即可
	#内层循环
	for($j=2; $j<$i; $j++){   #$j：除数
		if($i%$j===0){
			break;  //有一个比$i小的数可以整除$i=>$i不是质数
		}  //此处不能添加else！
	}
	//此时，$j的值有两种可能：
	//1) $j<$i：说明中间执行了一次break=>不是质数
	//2) $j==$i：说明没有任何一个比$i小的数可以整除$i=>是质数
	if($j===$i){
		echo $i."&nbsp;&nbsp;";
	}
}