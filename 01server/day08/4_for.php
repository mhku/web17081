<?php
/*
练习：使用for循环打印出所有的三位“水仙花数”——各个位上的数字的立方和等于该数本身，如153==1*1*1+5*5*5+3*3*3，

提示：循环从100开始到999结束，设法取出每个数字的各个位上的数字
*/

for($i=100;  $i<1000;  $i++){
	//$i: 是一个三位的数字
	//取出$i个位上的数字
	$ge = $i%10;

	//取出$i十位上的数字
	#$shi = ($i-$ge)/10%10;
	$shi = ($i%100-$ge)/10;
	
	//取出$i百位上的数字
	#$bai = ($i-$i%100)/100;
	$bai = (int)($i/100);  #强制类型转换 2.53=>2

	//各个位上的立方和是否等于$i
	if($i === $ge*$ge*$ge+$shi*$shi*$shi+$bai*$bai*$bai){
		echo "$i &nbsp;&nbsp;";
	}
}
